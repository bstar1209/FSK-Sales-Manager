    一般の消費者は会員とはなれません.なお、会員が通常の手順にて当サイトにログインを許可されたこと、あるいはその他媒体を通じて当社の定める方法により当社サービスの利用が可能になったことをもって、当社がその利用を承認したものとみなします.
</p>
<p class="page-highlight" style="font-weight: 300">
