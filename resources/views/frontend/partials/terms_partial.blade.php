    1  会員は、ＩＤおよびパスワードを自己の責任において管理、使用するものとします.<br>
    2  ＩＤおよびパスワードは、会員本人だけが使用できるものとし、いかなる第三者に対しても、譲渡・売買・名義の変更・貸与・質権その他の担保に供する等の行為をしてはならないものとします.<br>
    3  第三者が会員のＩＤおよびパスワードを不正に利用し、会員が損害を被ったとしても、当社は一切の責任を負わないものとします.会員は、自己のＩＤおよびパスワードを使用してなされた当社サービスでの商品購入その他による一切の債務を負担するものとします.<br>
</p>
